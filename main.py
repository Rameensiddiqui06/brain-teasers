def start_quiz(selected_quiz):
    global quiz_data, current_question, score#made them global so they can be use throughout the program for tracking
    quiz_data = selected_quiz#iniliaze them as zero
    current_question = 0
    score = 0
    show_question()#responsible for displaying ques and possible answer //called here
    quiz_window.deiconify()#Restore window that was previously hidden and minimized
    root.withdraw()#This hides the main window that is the quiz homepage without destroying
def show_question():#responsible for displaying ques and possible answer
    global current_question
    Questions = quiz_data[current_question]#retrive the current question dictionary from the quiz data using current_question as index
    q_label.config(text=Questions["Question"])#update the question label(q_label) with the text/wording of current question
    choices = Questions["Choices"]
    for i in range(4):
        choice_buttons[i].config(text=choices[i], state="normal",bg="yellow")#update each button with the corresponding choice

    corr_incorr.config(text="")#clear any feedback about correctness
    next_button.config(state="disabled")#disable the next button so that we dont move to next question before answering current

def check_answer(choice_index):
    Question = quiz_data[current_question]#Retrieve current question from quiz data
    selected_choice = choice_buttons[choice_index].cget("text")#get the text of the selected choice button using choice index
    if selected_choice == Question["Answer"]:#checking if selected choice matches the correct answer
        #increment score if answer is correct
        global score
        score += 1
        score_label.config(text=f"SCORE: {score}/{len(quiz_data)}")#update the score label on screen if Correct
        corr_incorr.config(text="====CORRECT!====", foreground="green",background="yellow",font=("rockwell",16,"bold"))#update the feedback label to show correct
        choice_buttons[choice_index].config(bg="lawngreen")#changes backgroung color to green on clicking if its correct
    else:
        corr_incorr.config(text="====INCORRECT!====", foreground="red",background="yellow",font=("rockwell",16,"bold"))#update the feedback label to show incorrect
        choice_buttons[choice_index].config(bg="firebrick")#changes backgroung color to red on clicking if its incorrect

    for button in choice_buttons:
        button.config(state="disabled")#disable cchoice buttun for interaction
    next_button.config(state="normal")#enable next button to move to the next question


